package ch20;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;

public class ChatMgr3 {

	private DBConnectionMgr pool;
	public ChatMgr3() {
		pool = DBConnectionMgr.getInstance();
	}
		
	//로그인
	public boolean loginChk (String id, String pwd) {
		//db1 입력후 ctrl+space
		Connection con = null;//DB 연결 객체 (외워야함)
		PreparedStatement pstmt = null; //sql 문 만드는 객체
		ResultSet rs = null;//select 문 실행 결과값 리턴 객체
		String sql = null; // 변수명을 왜 sql로 했는가?
		boolean flag=false;
		try {
			con = pool.getConnection();
			sql = "select id from tblChat where id=? and pwd=?";
			pstmt = con.prepareStatement(sql);
			//? 물음표 세팅
			pstmt.setString(1, id);
			pstmt.setString(2, pwd);
			rs = pstmt.executeQuery();
			flag=rs.next();//조건에 맞는 결과값이 있으면 true, 없으면 false
		} catch (Exception e) {
			e.printStackTrace();
		} finally {
			pool.freeConnection(con, pstmt, rs);
		}
		return flag; // 결과괎 flag
	}
}
